        double frand_Rayleigh ( double S ) /////////////////////////////////////////////////////////
        // S is the parameter Sigma in Eq. (7.4.6) 
        { //////////////////////////////////////////////////////////////////////////////////////////
	        return S * sqrt ( - 2.0 * log ( brng () ) );
        } 
