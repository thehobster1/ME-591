        double frand_Gaussian ( double E, double S ) ///////////////////////////////////////////////
        // Here E is the mean, S is the standard deviation
        { //////////////////////////////////////////////////////////////////////////////////////////
	        return E + S * sqrt ( - 2.0 * log ( brng () ) ) * cos ( 2.0 * M_PI * brng () );
        } 
