	double frand_discrete_std (double *X, double *Pcum ) ///////////////////////////////
        // Pcum is an array of cumulative probabilities
	// Individual values are indexed by the index i = 0,...,N-1
	{ //////////////////////////////////////////////////////////////////////////////////////////	
	int	i = 0;
	
	double  Gamma = brng ();
		while ( Gamma > Pcum[i] ) i++;
		return X[i];
	} //////////////////////////////////////////////////////////////////////////////////////////
